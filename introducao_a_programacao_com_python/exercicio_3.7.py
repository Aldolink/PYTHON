# Fazer um programa que pede dois numeros inteiros.
# imprimir a soma desses dois numeros na tela.

# Solicita dois numeros inteiros.
num_1 = int(input("Digite o primeiro numero inteiro: "))
num_2 = int(input("Digite o segundo numero inteiro: "))
# Calcula a soma dos numeros que foram digitados.
soma = num_1 + num_2
# Imprime o resultado.
print("A soma desses numeros e: ", soma)


# Escrever um programa que leia um valor em metros
# e o exiba convertido em milimetros.

# Solicitando o valor em metros
metros = float(input("Digite o valor em metros: "))
# Converte metros para milimetros
milimetros = metros * 1000
# Exibindo o valor convertido
print(f"O valor em metros e:, {milimetros} mm")